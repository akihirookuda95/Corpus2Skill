"""Evaluation metrics for Corpus2Skill.

Lexical metrics (F1, BLEU, ROUGE) plus LLM-judge metrics (Factuality, Context Recall).
Prompts follow the WixQA evaluation protocol (arXiv:2505.08643).
"""

from __future__ import annotations

import collections
import json
import re
import time


def _tokenize(text: str) -> list[str]:
    """Simple whitespace + punctuation tokenizer."""
    return re.findall(r"\w+", text.lower())


def compute_f1(prediction: str, reference: str) -> float:
    """Token-level F1 score."""
    pred_tokens = _tokenize(prediction)
    ref_tokens = _tokenize(reference)
    if not pred_tokens or not ref_tokens:
        return 0.0

    pred_counter = collections.Counter(pred_tokens)
    ref_counter = collections.Counter(ref_tokens)
    common = sum((pred_counter & ref_counter).values())

    if common == 0:
        return 0.0
    precision = common / len(pred_tokens)
    recall = common / len(ref_tokens)
    return 2 * precision * recall / (precision + recall)


def compute_bleu(prediction: str, reference: str) -> float:
    """Sentence-level BLEU with smoothing."""
    from nltk.translate.bleu_score import sentence_bleu, SmoothingFunction
    pred_tokens = _tokenize(prediction)
    ref_tokens = _tokenize(reference)
    if not pred_tokens or not ref_tokens:
        return 0.0
    smoothing = SmoothingFunction().method1
    return sentence_bleu([ref_tokens], pred_tokens, smoothing_function=smoothing)


def compute_rouge(prediction: str, reference: str) -> dict[str, float]:
    """ROUGE-1 and ROUGE-2 F-measure."""
    from rouge_score import rouge_scorer
    scorer = rouge_scorer.RougeScorer(["rouge1", "rouge2"], use_stemmer=True)
    scores = scorer.score(reference, prediction)
    return {
        "rouge1": scores["rouge1"].fmeasure,
        "rouge2": scores["rouge2"].fmeasure,
    }


